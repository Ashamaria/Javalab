public class Operators 
{
   public static void main(String a[])
   {       
     int x = 11,y = 20;
     System.out.println("x =" + x);
     System.out.println("y =" + y);
     System.out.println("INCREMENT OPERATORS ARE ");
     System.out.println("x++ = " + x++);
     System.out.println("y++ = " + y++);
     System.out.println("x = " + x);
     System.out.println("y = " + y);
     System.out.println("++x = " + ++x);
     System.out.println("++y = " + ++y);
     System.out.println("x = " +x);
     System.out.println("y = " +y);
     System.out.println("DECREMENT OPERATORS ARE ");
     System.out.println("x-- = " + x--);
     System.out.println("y-- = " + y--);
     System.out.println("x = " + x);
     System.out.println("y = " + y);
     System.out.println("--x = " + --x);
     System.out.println("--y = " + --y);
     System.out.println("x = " +x);
     System.out.println("y = " +y);
     

    }
}