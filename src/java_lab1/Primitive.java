public class Primitive 
{
    
 int i;
 short s;
 String str;
 float f;
 char c;
 long l;
 double d;
 boolean b;
 public static void main(String a[]) 
{ 
    Primitive m = new Primitive();
	System.out.println("int i ="+ m.i);
	System.out.println("short s ="+ m.s);
	System.out.println("string str =" + m.str);
	System.out.println("float f ="+ m.f);
	System.out.println("char c ="+ m.c);
	System.out.println("long l ="+ m.l);
	System.out.println("double d ="+ m.d);
	System.out.println("boolean b =" + m.b);

    
} 
}

